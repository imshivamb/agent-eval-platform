"""Abstractions for Large Language Models (LLMs) used in evaluations."""

from abc import ABC, abstractmethod
from typing import Any, List, Optional, Union


class BaseLLM(ABC):
    """Abstract base class representing an LLM client wrapper."""

    @abstractmethod
    def generate(self, prompt: str, system_instruction: Optional[str] = None) -> str:
        """Generates a text completion for the given prompt.

        Args:
            prompt: The user prompt.
            system_instruction: Optional system instruction/system prompt.

        Returns:
            The raw string response generated by the model.
        """
        pass


class MockLLM(BaseLLM):
    """A mock LLM wrapper for testing and offline execution."""

    def __init__(self, responses: Union[str, List[str]]):
        """Initializes the MockLLM.

        Args:
            responses: A single response string or a list of response strings
                to return sequentially on each generate call.
        """
        self.responses = [responses] if isinstance(responses, str) else list(responses)
        self.call_count = 0

    def generate(self, prompt: str, system_instruction: Optional[str] = None) -> str:
        if not self.responses:
            return "Mock response"
        response = self.responses[self.call_count % len(self.responses)]
        self.call_count += 1
        return response


class OpenAILLM(BaseLLM):
    """OpenAI API wrapper client."""

    def __init__(self, model_name: str = "gpt-4o", api_key: Optional[str] = None):
        """Initializes the OpenAILLM.

        Args:
            model_name: The target model ID.
            api_key: The OpenAI API key. If not provided, will read from the environment.
        """
        self.model_name = model_name
        self.api_key = api_key

    def generate(self, prompt: str, system_instruction: Optional[str] = None) -> str:
        try:
            # pyrefly: ignore [missing-import]
            import openai
        except ImportError:
            raise ImportError(
                "The 'openai' library is required to use OpenAILLM. "
                "Please install it using 'pip install openai'."
            )

        client = openai.OpenAI(api_key=self.api_key)
        messages = []
        if system_instruction:
            messages.append({"role": "system", "content": system_instruction})
        messages.append({"role": "user", "content": prompt})

        response = client.chat.completions.create(
            model=self.model_name,
            messages=messages,
        )
        return response.choices[0].message.content or ""


class GeminiLLM(BaseLLM):
    """Google Gemini API wrapper client."""

    def __init__(
        self, model_name: str = "gemini-1.5-pro", api_key: Optional[str] = None
    ):
        """Initializes the GeminiLLM.

        Args:
            model_name: The target Gemini model name.
            api_key: The Gemini API key. If not provided, configuration is skipped.
        """
        self.model_name = model_name
        self.api_key = api_key

    def generate(self, prompt: str, system_instruction: Optional[str] = None) -> str:
        try:
            # pyrefly: ignore [missing-import]
            import google.generativeai as genai
        except ImportError:
            raise ImportError(
                "The 'google-generativeai' library is required to use GeminiLLM. "
                "Please install it using 'pip install google-generativeai'."
            )

        if self.api_key:
            genai.configure(api_key=self.api_key)

        config: Any = {}
        if system_instruction:
            config["system_instruction"] = system_instruction

        model = genai.GenerativeModel(self.model_name, **config)
        response = model.generate_content(prompt)
        return response.text or ""
