"""Evaluator for constraint satisfaction using LLM judgment."""

import json
import yaml
from framework.models import AgentOutput, Benchmark, DimensionScore
from framework.evaluation.base import BaseEvaluator
from framework.llms import BaseLLM, Message


def _parse_json_response(text: str) -> dict:
    """Extracts and parses a JSON object from raw LLM output.

    Handles wrapping blocks like ```json ... ``` robustly.

    Args:
        text: Raw text generated by the model.

    Returns:
        The parsed dictionary.
    """
    cleaned = text.strip()
    if cleaned.startswith("```json"):
        cleaned = cleaned[7:]
    elif cleaned.startswith("```"):
        cleaned = cleaned[3:]
    if cleaned.endswith("```"):
        cleaned = cleaned[:-3]
    return json.loads(cleaned.strip())


class ConstraintEvaluator(BaseEvaluator):
    """Evaluates the 'Constraint Satisfaction' dimension of an agent's output.

    Uses an LLM Judge to determine if all constraints defined in the benchmark
    scenario are successfully met.
    """

    def __init__(self, llm: BaseLLM):
        """Initializes the ConstraintEvaluator.

        Args:
            llm: An instance of a BaseLLM provider wrapper.
        """
        self.llm = llm

    def evaluate(
        self,
        benchmark: Benchmark,
        output: AgentOutput,
    ) -> DimensionScore:
        """Evaluates whether the agent satisfied the benchmark constraints.

        Args:
            benchmark: The scenario benchmark details.
            output: The response/data generated by the AI agent.

        Returns:
            A DimensionScore detailing constraint satisfaction results.
        """
        # Serialize constraints for context
        constraints_str = (
            yaml.dump(benchmark.constraints, default_flow_style=False)
            if benchmark.constraints
            else "No explicit constraints defined."
        )

        system_instruction = (
            "You are an objective AI evaluator checking if an agent's output satisfies the explicit constraints "
            "of a benchmark scenario.\n"
            "Analyze the constraints and the agent output carefully.\n\n"
            "Return a JSON object containing exactly these fields:\n"
            "{\n"
            '  "score": <integer from 0 to 100 representing the percentage of constraints met>,\n'
            '  "reason": "<detailed justification detailing which constraints passed and which failed>"\n'
            "}\n"
            "Do not include any markdown syntax, explanation, or code blocks outside the JSON."
        )

        user_prompt = (
            f"Benchmark Name: {benchmark.name}\n"
            f"Benchmark Description: {benchmark.description}\n\n"
            f"### Explicit Constraints:\n{constraints_str}\n\n"
            f"### Agent Output:\n{output.content}\n"
        )

        messages = [
            Message(role="system", content=system_instruction),
            Message(role="user", content=user_prompt),
        ]

        response_text = ""
        try:
            response = self.llm.generate(messages)
            response_text = response.text
            parsed = _parse_json_response(response_text)
            score = float(parsed["score"])
            reason = str(parsed["reason"])
        except Exception as e:
            from framework.exceptions import EvaluationError
            raise EvaluationError(
                f"Failed to generate or parse LLM evaluation response: {e}. Raw response: {response_text}"
            ) from e

        return DimensionScore(
            dimension="Constraint Satisfaction",
            score=score,
            reason=reason,
        )
