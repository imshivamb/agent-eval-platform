"""Base class for all LLM-based evaluators."""

from abc import abstractmethod
import json
from typing import Dict, List
from framework.evaluation.base import BaseEvaluator
from framework.exceptions import EvaluationError
from framework.models import AgentOutput, Benchmark, DimensionScore
from framework.llms import BaseLLM, Message


class BaseLLMEvaluator(BaseEvaluator):
    """Abstract base class implementing orchestration and parsing for LLM judges."""

    def __init__(self, llm: BaseLLM):
        """Initializes the BaseLLMEvaluator.

        Args:
            llm: An instance of a BaseLLM provider wrapper.
        """
        self.llm = llm

    @property
    @abstractmethod
    def dimension(self) -> str:
        """The dimension name this evaluator evaluates.

        Returns:
            A string containing the evaluation dimension name.
        """
        pass

    @abstractmethod
    def build_prompt(
        self, benchmark: Benchmark, output: AgentOutput
    ) -> List[Message]:
        """Constructs the prompt messages for the LLM.

        Args:
            benchmark: The scenario benchmark details.
            output: The response/data generated by the AI agent.

        Returns:
            A list of Message objects.
        """
        pass

    def _parse_json_response(self, text: str) -> dict:
        """Extracts and parses a JSON object from raw LLM output.

        Handles wrapping blocks like ```json ... ``` robustly.

        Args:
            text: Raw text generated by the model.

        Returns:
            The parsed dictionary.

        Raises:
            ValueError: If parsing fails.
        """
        cleaned = text.strip()
        if cleaned.startswith("```json"):
            cleaned = cleaned[7:]
        elif cleaned.startswith("```"):
            cleaned = cleaned[3:]
        if cleaned.endswith("```"):
            cleaned = cleaned[:-3]
        return json.loads(cleaned.strip())

    def evaluate(
        self,
        benchmark: Benchmark,
        output: AgentOutput,
    ) -> DimensionScore:
        """Orchestrates LLM prompt execution, response parsing, and score wrapping.

        Args:
            benchmark: The scenario benchmark details.
            output: The response/data generated by the AI agent.

        Returns:
            A DimensionScore containing the final rating.

        Raises:
            EvaluationError: If the model generation or JSON parsing fails.
        """
        messages = self.build_prompt(benchmark, output)
        response_text = ""

        try:
            response = self.llm.generate(messages)
            response_text = response.text
            parsed = self._parse_json_response(response_text)
            score = float(parsed["score"])
            reason = str(parsed["reason"])
        except Exception as e:
            raise EvaluationError(
                f"Failed to generate or parse LLM evaluation response: {e}. "
                f"Raw response: {response_text}"
            ) from e

        return DimensionScore(
            dimension=self.dimension,
            score=score,
            reason=reason,
        )
