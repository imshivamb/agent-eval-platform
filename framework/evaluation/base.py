"""Base interface for all evaluation strategies."""

from abc import ABC, abstractmethod
from framework.models import AgentOutput, Benchmark, DimensionScore


class BaseEvaluator(ABC):
    """Abstract base class defining the interface for evaluation strategies.

    Evaluators assess an agent's output against a benchmark scenario for a
    single specific evaluation dimension.
    """

    @abstractmethod
    def evaluate(
        self,
        benchmark: Benchmark,
        output: AgentOutput,
    ) -> DimensionScore:
        """Evaluates a single dimension of an agent's output against a benchmark.

        Args:
            benchmark: The scenario benchmark details.
            output: The response/data generated by the AI agent.

        Returns:
            A DimensionScore containing the dimension name, score, and reasoning.
        """
        pass
