"""Engine for orchestrating benchmark evaluations across multiple dimensions."""

from typing import Dict
from framework.exceptions import EvaluationError
from framework.models import (
    AgentOutput,
    Benchmark,
    DimensionScore,
    EvaluationProfile,
    EvaluationResult,
)
from framework.evaluation.base import BaseEvaluator


class EvaluationEngine:
    """Orchestrates the evaluation of an agent's output against a benchmark scenario.

    Uses an EvaluationProfile to weigh scores across multiple dimensions and
    calculates the final evaluation outcome.
    """

    def __init__(self, evaluators: Dict[str, BaseEvaluator]):
        """Initializes the EvaluationEngine.

        Args:
            evaluators: A dictionary mapping dimension names to BaseEvaluator instances.
        """
        self.evaluators = evaluators

    def evaluate(
        self,
        benchmark: Benchmark,
        output: AgentOutput,
        profile: EvaluationProfile,
    ) -> EvaluationResult:
        """Runs the evaluation pipeline for the given benchmark and agent output.

        Args:
            benchmark: The benchmark scenario.
            output: The response/data generated by the AI agent.
            profile: The evaluation profile specifying dimension weights.

        Returns:
            An EvaluationResult containing the dimension breakdown and final status.

        Raises:
            EvaluationError: If a dimension specified in the profile has no registered evaluator,
                is missing from the benchmark criteria, or returns an inconsistent dimension name.
        """
        if not profile.weights:
            raise EvaluationError(
                f"Evaluation profile '{profile.name}' contains no weights."
            )

        # Validate that all profile dimensions exist in the benchmark criteria
        for dimension in profile.weights.keys():
            if dimension not in benchmark.evaluation_criteria:
                raise EvaluationError(
                    f"Dimension '{dimension}' from profile '{profile.name}' "
                    f"is not defined in benchmark '{benchmark.benchmark_id}' evaluation criteria."
                )

        dimension_scores = []
        weighted_score_sum = 0.0
        total_weight = 0.0

        for dimension, weight in profile.weights.items():
            evaluator = self.evaluators.get(dimension)
            if not evaluator:
                raise EvaluationError(
                    f"No evaluator registered for dimension '{dimension}' "
                    f"in profile '{profile.name}'."
                )

            try:
                score = evaluator.evaluate(benchmark, output)
            except Exception as e:
                raise EvaluationError(
                    f"Evaluator failed for dimension '{dimension}': {e}"
                ) from e

            # Safety check: Ensure the evaluator returned the correct dimension score
            if score.dimension != dimension:
                raise EvaluationError(
                    f"Evaluator returned score for dimension '{score.dimension}' "
                    f"instead of the expected dimension '{dimension}'."
                )

            dimension_scores.append(score)
            weighted_score_sum += score.score * weight
            total_weight += weight

        if total_weight <= 0:
            raise EvaluationError(
                "Total weight of evaluation dimensions must be greater than zero."
            )

        overall_score = weighted_score_sum / total_weight
        passed = overall_score >= profile.pass_threshold

        return EvaluationResult(
            benchmark_id=benchmark.benchmark_id,
            benchmark_name=benchmark.name,
            overall_score=overall_score,
            dimension_scores=dimension_scores,
            passed=passed,
        )
