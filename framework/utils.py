"""Shared helper utility functions for the framework."""

import json
from typing import Any


def parse_json_markdown(text: str) -> Any:
    """Extracts and parses a JSON structure from raw LLM output.

    Handles wrapping blocks like ```json ... ``` robustly.

    Args:
        text: Raw text generated by the model.

    Returns:
        The parsed Python dictionary, list, or primitive.

    Raises:
        ValueError: If JSON decoding fails.
    """
    cleaned = text.strip()
    if cleaned.startswith("```json"):
        cleaned = cleaned[7:]
    elif cleaned.startswith("```"):
        cleaned = cleaned[3:]
    if cleaned.endswith("```"):
        cleaned = cleaned[:-3]
    try:
        return json.loads(cleaned.strip())
    except json.JSONDecodeError as e:
        raise ValueError(f"Invalid JSON string: {e}") from e
